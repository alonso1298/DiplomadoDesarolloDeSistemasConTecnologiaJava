public class Operadores {
    public static void main(String[] args) {
        System.out.println("Operadores aritmeticos");
        int numero = 100;
        int otro = 11;

        System.out.println(numero + otro); // Suma
        System.out.println(numero - otro); // Rest
        System.out.println(numero * otro); // Multiplication
        System.out.println(numero / otro); // Division
        System.out.println(numero % otro); // Residuo de dividir un numero entre otro
    }
}
